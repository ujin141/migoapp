let patchedCount = 0;

for (const [lang, sections] of Object.entries(PATCHES)) {
  const filePath = require('path').join(__dirname, 'src/i18n/locales', `${lang}.ts`);
  if (!require('fs').existsSync(filePath)) { console.log(`SKIP: ${lang}.ts`); continue; }

  let content = require('fs').readFileSync(filePath, 'utf8');
  let changed = false;

  for (const [section, replacement] of Object.entries(sections)) {
    const { start, end } = MARKERS[section];
    const startIdx = content.indexOf(start);
    const endIdx = content.indexOf(end, startIdx);
    if (startIdx === -1 || endIdx === -1) { console.log(`  ${lang}/${section}: skip`); continue; }
    content = content.slice(0, startIdx) + replacement + content.slice(endIdx + end.length);
    changed = true;
    console.log(`  ✓ ${lang}/${section}`);
  }

  if (changed) { require('fs').writeFileSync(filePath, content, 'utf8'); patchedCount++; console.log(`[DONE] ${lang}.ts`); }
}
console.log(`\nDone. ${patchedCount} file(s) updated.`);
