let patchedCount = 0;

for (const [lang, sections] of Object.entries(PATCHES)) {
  const filePath = path.join(LOCALES, `${lang}.ts`);
  if (!fs.existsSync(filePath)) {
    console.log(`SKIP: ${lang}.ts not found`);
    continue;
  }

  let content = fs.readFileSync(filePath, 'utf8');
  let changed = false;

  for (const [section, replacement] of Object.entries(sections)) {
    const { start, end } = MARKERS[section];
    const startIdx = content.indexOf(start);
    const endIdx = content.indexOf(end, startIdx);

    if (startIdx === -1 || endIdx === -1) {
      console.log(`  ${lang}/${section}: already translated or not found, skipping`);
      continue;
    }

    const fullEnd = endIdx + end.length;
    content = content.slice(0, startIdx) + replacement + content.slice(fullEnd);
    changed = true;
    console.log(`  ✓ ${lang}/${section}: patched`);
  }

  if (changed) {
    fs.writeFileSync(filePath, content, 'utf8');
    patchedCount++;
    console.log(`[DONE] ${lang}.ts updated`);
  }
}

console.log(`\nDone. ${patchedCount} file(s) updated.`);
