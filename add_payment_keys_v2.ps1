$successCount = 0
$failCount = 0

foreach ($lang in $translations.Keys) {
  $filePath = Join-Path $localeDir "$lang.ts"
  
  if (-not (Test-Path $filePath)) {
    Write-Warning "File not found: $filePath"
    $failCount++
    continue
  }
  
  $content = Get-Content $filePath -Raw -Encoding UTF8
  
  # Check if keys already exist
  if ($content -match '"testMode"') {
    Write-Host "[$lang] Already has testMode key, skipping." -ForegroundColor Yellow
    continue
  }
  
  $t = $translations[$lang]
  $testMode = $t.testMode
  $group = $t.group
  $back = $t.back
  
  # Pattern: find "noMethod": "..." followed by closing brace of payment section
  # We need to find the exact closing of payment section
  $newContent = $content -replace '("noMethod":\s*"[^"]*")\s*\n(\s*\},\s*\n\s*"groupModal")', "`$1,`n    `"testMode`": `"$testMode`",`n    `"group`": `"$group`",`n    `"back`": `"$back`"`n`$2"
  
  if ($newContent -eq $content) {
    Write-Warning "[$lang] Pattern not found, trying alternative pattern..."
    # Try without groupModal lookahead
    $newContent = $content -replace '("noMethod":\s*"[^"]*")\r?\n(\s*\})', "`$1,`n    `"testMode`": `"$testMode`",`n    `"group`": `"$group`",`n    `"back`": `"$back`"`n`$2"
    
    if ($newContent -eq $content) {
      Write-Warning "[$lang] Could not apply payment keys!"
      $failCount++
      continue
    }
  }
  
  # Write back with UTF8 no BOM
  $utf8NoBom = New-Object System.Text.UTF8Encoding $false
  [System.IO.File]::WriteAllText((Resolve-Path $filePath), $newContent, $utf8NoBom)
  Write-Host "[$lang] Payment keys added successfully." -ForegroundColor Green
  $successCount++
}

Write-Host ""
Write-Host "=== DONE ===" -ForegroundColor Cyan
Write-Host "Success: $successCount files" -ForegroundColor Green
Write-Host "Failed:  $failCount files" -ForegroundColor Red
